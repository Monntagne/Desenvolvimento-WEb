//6 - Calcular a área de um retângulo
let retangulo_altura = Number(prompt(`Iremos calcular a área do retangulo, digite a altura:`))
let retangulo_largura = Number(prompt(`Digite a largura:`))
let area_retangulo = retangulo_altura * retangulo_largura
alert(`A área do retangulo é de ${area_retangulo}`)