//8 - Calcular a área de um losango
let diagonal_maior = Number(prompt(`Iremos calcular a área de um losango, digite o tamanho da diagonal maior:`))
let diagonal_menor = Number(prompt(`digite a diagonal menor:`))
let multiplicacao_losango = diagonal_maior * diagonal_menor
let divisao_losango = multiplicacao_losango / 2
alert(`A área do losango é de ${divisao_losango} cm²`)
