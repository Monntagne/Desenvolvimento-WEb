//7 - Calcular a área de um triângulo
let triangulo_largura = Number(prompt(`Iremos calcular a área de um triangulo, digite a largura:`))
let triangulo_altura = Number(prompt(`digite a altura:`))
let multiplicacao_triangulo = triangulo_largura * triangulo_altura
let divisao_triangulo = multiplicacao_triangulo / 2
alert(`A área do triangulo é de ${divisao_triangulo}`)