// 7 - Faça um programa para calcular área de um losango 
alert(`Vamos calcular a área de um losango!`)
let diagonalmaior = Number(prompt(`Digite a medida da diagonal maior!`))
let diagonalmenor = Number(prompt(`Digite a medida da diagonal menor!`))
let resultado = (diagonalmaior * diagonalmenor)/2
alert(`A área do losango, com a diagonal maior medindo ${diagonalmaior} cm, e com a diagonal menor medindo ${diagonalmenor} cm, é de ${resultado} cm²`)