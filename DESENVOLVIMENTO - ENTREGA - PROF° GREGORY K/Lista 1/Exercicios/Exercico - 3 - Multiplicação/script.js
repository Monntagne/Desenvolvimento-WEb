// 3 - Faça um programa que receba dois valores e faça a multiplicação 
alert(`Vamos realizar uma multipilicação`)
let numero1 = Number(prompt(`Digite um número!`))
let numero2 = Number(prompt(`Digite mais um número!`))
let resultado = numero1 * numero2
alert(`A multiplicação de ${numero1} x ${numero2} é igual a ${resultado}`)