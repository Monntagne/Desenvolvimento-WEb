// 6 - Faça um programa para calcular área de um retângulo 
alert(`Vamos calcular a área do retângulo!`)
let base = Number(prompt(`Informe a medida da base:`))
let altura = Number(prompt(`Informe a medida da altura:`))
let resultado = base * altura
alert(`A área do retângulo, com base medindo ${base} cm, e com altura medindo ${altura} cm, é de ${resultado} cm²`)