// 8 - Faça um programa para calcular área de um triângulo
alert(`Vamos calcular a área de um triângulo`)
let altura = Number(prompt(`Digite a medida da altura do triângulo`))
let base = Number(prompt(`Digite a medida da base do triângulo`))
let resultado = (base * altura)/2
alert(`A área do triângulo, com a base medindo ${base} cm e altura medindo ${altura} cm, é de ${resultado} cm²`)